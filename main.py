# Admin decorator function
def admin_only(func):
    @wraps(func)
    def wrapper_func(*args, **kwargs):
        try:
            if current_user.id == 1:
                return func(*args, **kwargs)
            return abort(403)
        except AttributeError:
            return abort(403)
    return wrapper_func


