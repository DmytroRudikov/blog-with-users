@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        user = User.query.filter_by(email=form.email.data).first()
        if user is not None:
            flash("You've already signed up with this email, use log in instead.")
            return redirect(url_for("login"))
        register_data = {
            field: value.data for field, value in form._fields.items()
            if field not in ["submit", "csrf_token", "password"]
        }
        hash_salted_password = generate_password_hash(
            password=form.password.data, method="pbkdf2:sha256", salt_length=10
        )
        register_data["password"] = hash_salted_password
        new_user = User(**register_data)
        db.session.add(new_user)
        db.session.commit()
        login_user(new_user)
        return redirect(url_for("get_all_posts"))
    return render_template("register.html", form=form)


@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        email = form.email.data
        password = form.password.data
        user = User.query.filter_by(email=email).first()
        if user is None:
            flash("That email does not exist, please try again.")
            return redirect(url_for("login"))
        else:
            password_checked = check_password_hash(pwhash=user.password, password=password)
            if password_checked:
                login_user(user)
                return redirect(url_for("get_all_posts"))
            flash("Password incorrect, please try again.")
            return redirect(url_for("login"))
    return render_template("login.html", form=form)


